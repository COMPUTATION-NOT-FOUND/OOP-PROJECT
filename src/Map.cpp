Map::Map() {
    tex="../assets/map.png";
    TextureManager::LoadTexture(tex);
    LoadMap(lvl1);
    src.x = 0;
    src.y = 0;
    src.h = 30;
    src.w = 31;

    dest.x = 0;
    dest.y = 0;
    dest.w = 40;
    dest.h = 32;
}

void Map::LoadMap(int arr[20][20]) {
    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 20; column++) {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::DrawMap() {
    for (int row = 0; row < 20; row++) {
        for (int column = 0; column < 20; column++) {
            src.x = (map[row][column] % 100) * 33;
            src.y = (map[row][column] / 100) * 33;
            dest.x = column * 40;
            dest.y = row * 32;
            TextureManager::Draw(tex, &src, &dest);
        }
    }
}

